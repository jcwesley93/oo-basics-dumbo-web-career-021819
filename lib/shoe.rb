class Shoe 
  
  def initialize(brand)
    @brand = brand
  end
  
  def brand=(brand)
    @brand = "Nike"
  end
  
  def brand
    @brand
  end
  
  def color=(color)
    @color = "red"
  end
  
  def color
    @color
  end
  
  def size=(size)
    @size = 9.5
  end
  
  def size 
    @size
  end
  
end

